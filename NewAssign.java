public class NewAssign {
    public static void main(String[]args){
        int x=9;
        int y=5;
        x+=y;
        System.out.println("x:"+x);;
        x-=y;
        System.out.println("x:"+x);;
        x*=y;
        System.out.println("x:"+x);;
        x/=y;
        System.out.println("x:"+x);;
        x%=y;
        System.out.println("x:"+x);;

    }
}
