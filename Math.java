



public class Math {
    public static void main(String[] args) {
        int x=5;
        int y=3;
        int max=java.lang.Math.max(x, y);
        System.out.println("Maximum is:"+max);
        int min=java.lang.Math.min(x, y);
        System.out.println("Minimum is:"+min);
        int absoloute=java.lang.Math.abs(y);
        System.out.println("Absoloute value:"+absoloute);
        double power=java.lang.Math.pow(x, y);
        System.out.println("Power number:"+power);
        int round=java.lang.Math.round(8.5f);
        System.out.println("Round Number is:"+round);
        double pi= java.lang.Math.PI;
        System.out.println("pi is:"+pi);

    }

}
